export class Cat {
    id: number;
    name: string;
    length: number;
    weight: number;
    breed: string;

    constructor(id: number, name: string, length: number, weight: number, breed: string) {
        this.id = id;
        this.name = name;
        this.length = length;
        this.weight = weight;
        this.breed = breed;
      }
}